    val encrypt_Channel_Code:MutableList<Int> by lazy {

        val list = mutableListOf<Int>()

        val n = Random().nextInt(256)

        val n1 = (n + 45) % 256

        val subs = NUM_CODE.map {
            val str = it.toString()
            val t = str.toInt()
            return@map t xor n
        }

        val result = subs.reduce { total, next -> total + next }

        list.addAll(subs)

        val s = (result + n1) % 256

        list.add(s)

        list.add(n1)

        return@lazy list
    }

